// tipos de funções

//declarativas
function funcao(){
    console.log('Sou uma mensagem de uma função declarativa');
}

funcao();

// expressão de funcao
// com nomeação

var funcao1 = function(){
    console.log('Sou uma mensagem de funcao de expressão!');
}

funcao1();

//arrow function
var funcao2 = () =>{
    console.log('Sou um arrow function');
}

funcao2();

